// Ulrich Winkler
const playBtnWinkler = document.getElementById("playBtnWinkler");
const videoWinkler = document.getElementById("videoWinkler");

videoWinkler.addEventListener("click", function () {
  if (videoWinkler.paused == true) {
    // Play the video
    videoWinkler.play();
    playBtnWinkler.classList.add("d-none");
  } else {
    // Pause the video
    videoWinkler.pause();
    playBtnWinkler.classList.remove("d-none");
  }
});

playBtnWinkler.addEventListener("click", function () {
  if (videoWinkler.paused == true) {
    // Play the video
    videoWinkler.play();

    playBtnWinkler.classList.add("d-none");
  }
});

